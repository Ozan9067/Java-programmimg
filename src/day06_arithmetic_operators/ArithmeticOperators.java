package day06_arithmetic_operators;

public class ArithmeticOperators {
    public static void main(String[] args){
        System.out.println(5+10); //15
        System.out.println(10-3); //7
        System.out.println(3*3); //9
        System.out.println(15 / 5); //3
        System.out.println(13 % 5 ); //3 (the remainder)

        System.out.println(-2 * 2); // it will calculate negatives

        int chairs = 4 +2;
        System.out.println(chairs);

        int spoons =10 + 20 + 12;
        System.out.println(spoons);

        //There are 42 spoons in the kitchen

        System.out.println("There are " + spoons + " spoons in the kitchen");

    }
}
