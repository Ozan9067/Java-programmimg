package day07_arithmetic_operations_casting;

public class WarmUpClass {
    public static void main(String[] args) {
        String city1 = "Chicago";
        String city2 = "Atlanta";
        double ticketPrice = 216.80D; // optional to put D



        System.out.println("I bought a ticket to go from " + city1 +
                " to " + city2 + " for $" + ticketPrice + ".");

        String sentence = ("I bought a ticket to go from " + city1 +
                " to " + city2 + " for $" + ticketPrice + ".");

        System.out.println(sentence);

    }
}
